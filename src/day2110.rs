pub fn run(part: i8) {
    if part == 1 {
        part1();
    } else {
        part2();
    }
}

fn part1() {
    let mut answer = 0;

    for l in input().lines() {
        let mut stack = Vec::<char>::new();

        for c in l.trim().chars() {
            let s = starting(c);
            if s.is_none() {
                stack.push(c);
            } else if stack.pop() != Some(s.unwrap()) {
                answer += match c {
                    ')' => 3,
                    ']' => 57,
                    '}' => 1197,
                    '>' => 25137,
                    _ => 0,
                };
                break;
            }
        }
    }

    println!("{}", answer);
}

fn part2() {
    let mut answer: Vec<i64> = vec![];

    for l in input().lines() {
        let mut stack = Vec::<char>::new();

        for c in l.trim().chars() {
            let s = starting(c);
            if s.is_none() {
                stack.push(c); // this _is_ a starting char
            } else if stack.pop() != Some(s.unwrap()) {
                // corrupt
                stack.clear();
                break;
            }
        }

        if stack.len() > 0 {
            let mut stack_score: i64 = 0;
            while stack.len() > 0 {
                stack_score *= 5;
                stack_score += match ending(stack.pop().unwrap()).unwrap() {
                    ')' => 1,
                    ']' => 2,
                    '}' => 3,
                    '>' => 4,
                    _ => 0,
                }
            }
            answer.push(stack_score);
        }
    }

    answer.sort();
    println!("{}", answer[answer.len()/2]);
}

fn starting(c: char) -> Option<char> {
    match c {
        ')' => Some('('),
        ']' => Some('['),
        '>' => Some('<'),
        '}' => Some('{'),
        _ => None,
    }
}

fn ending(c: char) -> Option<char> {
    match c {
        '(' => Some(')'),
        '[' => Some(']'),
        '<' => Some('>'),
        '{' => Some('}'),
        _ => None,
    }
}

