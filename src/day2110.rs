pub fn run(part: i8) {
    if part == 1 {
        part1();
    } else {
        part2();
    }
}

fn part1() {
    let mut answer = 0;

    for l in input().lines() {
        let mut stack = Vec::<char>::new();

        for c in l.trim().chars() {
            let s = starting(c);
            if s.is_none() {
                stack.push(c);
            } else if stack.pop() != Some(s.unwrap()) {
                answer += score(c);
                break;
            }
        }
    }

    println!("{}", answer);
}

