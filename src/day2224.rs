#[derive(Debug)]
enum Space {
    Wall,
    Open,
    Left,
    Right,
    Up,
    Down,
}

#[derive(Debug)]
struct Valley {
    grid: Vec<Vec<Space>>,
    width: usize,
    height: usize,
    states: usize,
}

impl Valley {
    fn parse() -> Self {
        let mut grid = vec![];
        for line in input().lines() {
            let line = line.trim();
            let mut row = vec![];

            for c in line.chars() {
                row.push(match c {
                    '^' => Space::Up,
                    'v' => Space::Down,
                    '<' => Space::Left,
                    '>' => Space::Right,
                    '#' => Space::Wall,
                    _ => Space::Open,
                })
            }

            grid.push(row);
        }

        let width = grid[0].len() - 2;
        let height = grid.len() - 2;
        let states = lcm(width, height);

        Self {
            grid,
            width,
            height,
            states,
        }
    }
}

